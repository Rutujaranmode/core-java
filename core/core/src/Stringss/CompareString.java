package Stringss;

public class CompareString {
	
	public static void main(String[] args) {
		
		String name="hello";
		String surname="cello";
		
		
		if(name.compareTo(surname)==0)
		{
			System.out.println("Strings are equal");
		}
		else
		{
			System.out.println("Strings are not equal");
		}
	}

}
